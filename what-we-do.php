<!-- JavaScript for 3D Directional Door-Swing Hover Effect -->
<script>
(function() {
    'use strict';
    function initHeaderReveal(section) {
        var els = section.querySelectorAll('.wwd-subheading, .wwd-heading, .wwd-desc');
        if (!els.length) return;

        if (!('IntersectionObserver' in window)) {
            els.forEach(function (el) { el.classList.add('anim-in'); });
            return;
        }

        var obs = new IntersectionObserver(function (entries, o) {
            entries.forEach(function (entry) {
                if (entry.isIntersecting) {
                    entry.target.classList.add('anim-in');
                    o.unobserve(entry.target);
                }
            });
        }, { threshold: 0.2 });

        els.forEach(function (el) { obs.observe(el); });
    }
    function getHoverDirection(e, element) {
        const rect = element.getBoundingClientRect();
        const w = rect.width;
        const h = rect.height;
        
        // Normalized coordinate system with center of element as (0, 0)
        const x = (e.clientX - rect.left - (w / 2)) * (w > h ? (h / w) : 1);
        const y = (e.clientY - rect.top - (h / 2)) * (h > w ? (w / h) : 1);
        
        // Calculate heading sector: 0 = Top, 1 = Right, 2 = Bottom, 3 = Left
        const direction = Math.round((((Math.atan2(y, x) * (180 / Math.PI)) + 180) / 90) + 3) % 4;
        return direction;
    }

    function initDirectionalDoorHover() {
        const cards = document.querySelectorAll('.wwd-card-inner');
        const directions = ['top', 'right', 'bottom', 'left'];

        cards.forEach(card => {
            const door = card.querySelector('.wwd-card-door');
            if (!door) return;

            card.addEventListener('mouseenter', (e) => {
                const dirIndex = getHoverDirection(e, card);
                const dir = directions[dirIndex];

                // Disable transition to place the door instantly at the correct open hinge position
                door.style.transition = 'none';
                door.style.opacity = '0';

                switch(dir) {
                    case 'top':
                        door.style.transformOrigin = 'center top';
                        door.style.transform = 'rotateX(-90deg)';
                        break;
                    case 'right':
                        door.style.transformOrigin = 'right center';
                        door.style.transform = 'rotateY(-90deg)';
                        break;
                    case 'bottom':
                        door.style.transformOrigin = 'center bottom';
                        door.style.transform = 'rotateX(90deg)';
                        break;
                    case 'left':
                        door.style.transformOrigin = 'left center';
                        door.style.transform = 'rotateY(90deg)';
                        break;
                }

                // Force layout reflow
                door.offsetHeight;

                // Animate door to fully closed cover state and make it visible
                door.style.transition = 'transform 0.5s cubic-bezier(0.25, 1, 0.5, 1), opacity 0.4s ease';
                door.style.transform = 'rotateX(0deg) rotateY(0deg)';
                door.style.opacity = '1';
                card.classList.add('is-hovered');
            });

            card.addEventListener('mouseleave', (e) => {
                const dirIndex = getHoverDirection(e, card);
                const dir = directions[dirIndex];

                // Smoothly swing open and fade out
                door.style.transition = 'transform 0.5s cubic-bezier(0.25, 1, 0.5, 1), opacity 0.4s ease';
                door.style.opacity = '0';

                switch(dir) {
                    case 'top':
                        door.style.transformOrigin = 'center top';
                        door.style.transform = 'rotateX(-90deg)';
                        break;
                    case 'right':
                        door.style.transformOrigin = 'right center';
                        door.style.transform = 'rotateY(-90deg)';
                        break;
                    case 'bottom':
                        door.style.transformOrigin = 'center bottom';
                        door.style.transform = 'rotateX(90deg)';
                        break;
                    case 'left':
                        door.style.transformOrigin = 'left center';
                        door.style.transform = 'rotateY(90deg)';
                        break;
                }

                card.classList.remove('is-hovered');
            });
        });
    }
    function init() {
        document.querySelectorAll('.what-we-do-section').forEach(function (section) {
            initHeaderReveal(section);
        });
        initDirectionalDoorHover();
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', init);
    } else {
        init();
    }
})();
</script>

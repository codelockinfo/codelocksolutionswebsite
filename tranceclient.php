                                <strong>Development Store Limitations: </strong>
                                <li>
                                    Development stores have restrictions to ensure they are used solely for development purposes. Certain features might be unavailable.
                                </li>
                                <strong>Ownership Transfer:</strong>
                                <li>
                                    Only one account owner is permitted per development store. The initial owner is the Partner team member who created it. Upon transfer, ownership shifts to the client.
                                </li>
                                <strong>Staff Permissions:</strong>
                                <li>
                                    Up to 15 Partner team members can access a development store. Adding more requires removing existing staff.
                                </li>
                                <strong>Transfer Process:</strong>
                                <li>
                                    The transfer occurs through the Partner Dashboard, where you initiate the process and the client accepts ownership.
                                </li>
                            </ul>

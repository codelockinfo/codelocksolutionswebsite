<script>
document.addEventListener('DOMContentLoaded', function() {
    
    /* ── 1. Animated Buttons (Character Glide) ─────────────────────────── */
    function initDynamicButtons(buttonSelector) {
        const buttons = document.querySelectorAll(buttonSelector);
        buttons.forEach(button => {
            const textSpan = button.querySelector('span');
            if (!textSpan) return;

            const text = textSpan.textContent.trim();
            const icon = button.querySelector('i');

            button.innerHTML = '';

            const textWrapper = document.createElement('span');
            textWrapper.className = 'button-text-wrapper';

            const mother1 = document.createElement('span');
            mother1.className = 'span-mother';
            
            const mother2 = document.createElement('span');
            mother2.className = 'span-mother2';

            for (let i = 0; i < text.length; i++) {
                const char = text[i];
                const letter1 = document.createElement('span');
                const letter2 = document.createElement('span');

                if (char === ' ') {
                    letter1.innerHTML = '&nbsp;';
                    letter2.innerHTML = '&nbsp;';
                } else {
                    letter1.textContent = char;
                    letter2.textContent = char;
                }

                const delay = i * 0.03;
                letter1.style.transitionDelay = `${delay}s`;
                letter2.style.transitionDelay = `${delay}s`;

                mother1.appendChild(letter1);
                mother2.appendChild(letter2);
            }

            textWrapper.appendChild(mother1);
            textWrapper.appendChild(mother2);
            button.appendChild(textWrapper);
            
            if (icon) {
                const spaces = document.createTextNode(' ');
                button.appendChild(spaces);
                button.appendChild(icon);
            }
        });
    }
    
    // Initialize animated button on CTA
    initDynamicButtons('.cta-primary');
    initDynamicButtons('.proj-btn-primary');

    /* ── 2. Project Portfolio Interactive Filter ────────────────────── */
    const filterButtons = document.querySelectorAll('.filter-btn');
    const projectCards = document.querySelectorAll('.project-card');
    const noResultsBanner = document.getElementById('no-results-banner');
    
    filterButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            // Update active state in filter bar
            filterButtons.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            
            const filterValue = btn.getAttribute('data-filter');
            let visibleCount = 0;
            
            projectCards.forEach(card => {
                const cardCategory = card.getAttribute('data-category');
                
                if (filterValue === 'all' || cardCategory === filterValue) {
                    // Show matching card
                    card.classList.remove('fade-out');
                    card.classList.add('fade-in');
                    card.style.display = 'flex';
                    visibleCount++;
                } else {
                    // Hide non-matching card
                    card.classList.remove('fade-in');
                    card.classList.add('fade-out');
                    // Add delay to display none to match the transition duration
                    setTimeout(() => {
                        if (card.classList.contains('fade-out')) {
                            card.style.display = 'none';
                        }
                    }, 400);
                }
            });
            
            // Handle no results display
            setTimeout(() => {
                if (visibleCount === 0) {
                    noResultsBanner.style.display = 'block';
                    noResultsBanner.style.opacity = '1';
                    noResultsBanner.style.transform = 'scale(1)';
                } else {
                    noResultsBanner.style.display = 'none';
                }
            }, 400);
        });
    });

    /* ── 3. Number Counter Animation ────────────────────────────────── */
    const countElements = document.querySelectorAll('.stat-count');
    
    const startCounterAnimation = (entryVal) => {
        const val = parseInt(entryVal.getAttribute('data-val'));
        let currentCount = 0;
        const duration = 2000; // 2 seconds
        const stepTime = Math.abs(Math.floor(duration / val));
        
        const timer = setInterval(() => {
            currentCount += 1;
            entryVal.textContent = currentCount;
            if (currentCount >= val) {
                entryVal.textContent = val;
                clearInterval(timer);
            }
        }, Math.max(stepTime, 12));
    };

    const counterObserver = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const counters = entry.target.querySelectorAll('.stat-count');
                counters.forEach(counter => {
                    if (counter.textContent === '0') {
                        startCounterAnimation(counter);
                    }
                });
                observer.unobserve(entry.target);
            }
        });
    }, { threshold: 0.25 });

    const statsGrid = document.querySelector('.stats-grid');
    if (statsGrid) {
        counterObserver.observe(statsGrid);
    }

    /* ── 4. Custom Page Reveal Intersection Observer ────────────────── */
    const pageRevealObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible-reveal');
            } else {
                // Remove class to trigger animation again when scrolling back
                entry.target.classList.remove('visible-reveal');
            }
        });
    }, {
        threshold: 0.1,
        rootMargin: '0px 0px -40px 0px'
    });

    // Observe custom timeline items and elements
    document.querySelectorAll('.timeline-item, .projects-hero .fade-in-up, .filter-container').forEach(el => {
        pageRevealObserver.observe(el);
    });

});
</script>


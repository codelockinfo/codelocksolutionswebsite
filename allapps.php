<script>
(function () {
    'use strict';

    /* ── Scroll Reveal ───────────────────────────────── */
    function initScrollReveal() {
        if (!('IntersectionObserver' in window)) {
            document.querySelectorAll('[data-sr]').forEach(function(el) {
                el.classList.add('sr-visible');
            });
            return;
        }

        var obs = new IntersectionObserver(function(entries) {
            entries.forEach(function(entry) {
                if (entry.isIntersecting) {
                    entry.target.classList.add('sr-visible');
                } else {
                    entry.target.classList.remove('sr-visible');
                }
            });
        }, { threshold: 0.12 });

        document.querySelectorAll('[data-sr]').forEach(function(el) {
            obs.observe(el);
        });
    }

    /* ── Counter Animation ───────────────────────────── */
    function initCounters() {
        var counters = document.querySelectorAll('.stat-number[data-count]');
        if (!counters.length) return;

        var obs = new IntersectionObserver(function(entries) {
            entries.forEach(function(entry) {
                if (entry.isIntersecting) {
                    var el = entry.target;
                    var target = parseInt(el.getAttribute('data-count'), 10);
                    var duration = 1500;
                    var startTime = null;

                    function step(timestamp) {
                        if (!startTime) startTime = timestamp;
                        var progress = Math.min((timestamp - startTime) / duration, 1);
                        var ease = 1 - Math.pow(1 - progress, 3);
                        el.textContent = Math.floor(ease * target);
                        if (progress < 1) {
                            requestAnimationFrame(step);
                        } else {
                            el.textContent = target;
                        }
                    }

                    requestAnimationFrame(step);
                    obs.unobserve(el);
                }
            });
        }, { threshold: 0.5 });

        counters.forEach(function(el) { obs.observe(el); });
    }

    /* ── Filter Functionality ────────────────────────── */
    function initFilter() {
        var btns = document.querySelectorAll('.filter-btn');
        var cards = document.querySelectorAll('.app-full-card');
        var noResults = document.getElementById('no-results');

        btns.forEach(function(btn) {
            btn.addEventListener('click', function() {
                btns.forEach(function(b) { b.classList.remove('active'); });
                btn.classList.add('active');

                var filter = btn.getAttribute('data-filter');
                var visibleCount = 0;

                cards.forEach(function(card) {
                    var cat = card.getAttribute('data-category');
                    if (filter === 'all' || cat === filter) {
                        card.classList.remove('filtered-out');
                        visibleCount++;
                    } else {
                        card.classList.add('filtered-out');
                    }
                });

                if (noResults) {
                    noResults.style.display = visibleCount === 0 ? 'block' : 'none';
                }
            });
        });
    }

    /* ── 3D Tilt Effect on Cards ─────────────────────── */
    function initTilt() {
        document.querySelectorAll('.app-full-card').forEach(function(card) {
            card.addEventListener('mousemove', function(e) {
                var rect = card.getBoundingClientRect();
                var x = e.clientX - rect.left;
                var y = e.clientY - rect.top;
                var xc = rect.width / 2;
                var yc = rect.height / 2;
                var tiltX = -((y - yc) / yc) * 6;
                var tiltY = ((x - xc) / xc) * 6;
                card.style.transition = 'none';
                card.style.transform = 'rotateX(' + tiltX + 'deg) rotateY(' + tiltY + 'deg) scale3d(1.02,1.02,1.02)';
            });

            card.addEventListener('mouseleave', function() {
                card.style.transition = 'transform 0.5s cubic-bezier(0.25,1,0.5,1), opacity 0.4s ease';
                card.style.transform = 'rotateX(0deg) rotateY(0deg) scale3d(1,1,1)';
            });
        });
    }

    /* ── Animated CTA Buttons ────────────────────────── */
    function makeAnimatedButton(selector) {
        document.querySelectorAll(selector).forEach(function(button) {
            var wrapper = button.querySelector('.button-text-wrapper');
            if (!wrapper) return;

            var rawHTML = wrapper.innerHTML;
            var iconMatch = rawHTML.match(/<i [^>]+><\/i>/);
            var iconHTML = iconMatch ? iconMatch[0] : '';
            var text = wrapper.textContent.replace(/\s+/g, ' ').trim();

            var mother1 = document.createElement('span');
            mother1.className = 'span-mother';
            var mother2 = document.createElement('span');
            mother2.className = 'span-mother2';

            text.split('').forEach(function(char, i) {
                var l1 = document.createElement('span');
                var l2 = document.createElement('span');
                l1.innerHTML = char === ' ' ? '&nbsp;' : char;
                l2.innerHTML = char === ' ' ? '&nbsp;' : char;
                var delay = i * 0.033 + 's';
                l1.style.transitionDelay = delay;
                l2.style.transitionDelay = delay;
                mother1.appendChild(l1);
                mother2.appendChild(l2);
            });

            var newWrapper = document.createElement('span');
            newWrapper.className = 'button-text-wrapper';
            newWrapper.appendChild(mother1);
            newWrapper.appendChild(mother2);
            button.innerHTML = '';
            button.appendChild(newWrapper);

            if (iconHTML) {
                var iconEl = document.createElement('span');
                iconEl.innerHTML = iconHTML;
                button.appendChild(iconEl.firstChild);
            }
        });
    }

    /* ── Init ────────────────────────────────────────── */
    function init() {
        initScrollReveal();
        initCounters();
        initFilter();
        initTilt();
        makeAnimatedButton('.cta-primary');
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', init);
    } else {
        init();
    }
})();
</script>


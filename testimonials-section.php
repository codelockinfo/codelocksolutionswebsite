    <script>
    (function() {
        'use strict';

        function initHeaderReveal(section) {
            var els = section.querySelectorAll('.testimonials-subheading, .testimonials-headline, .testimonials-description');
            if (!els.length) return;

            if (!('IntersectionObserver' in window)) {
                els.forEach(function (el) { el.classList.add('anim-in'); });
                return;
            }

            var obs = new IntersectionObserver(function (entries, o) {
                entries.forEach(function (entry) {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('anim-in');
                    } else {
                        entry.target.classList.remove('anim-in');
                    }
                });
            }, { threshold: 0.2 });

            els.forEach(function (el) { obs.observe(el); });
        }

        function initTestimonialSlider(section) {
            var slider = section.querySelector('.testimonials-slider');
            var prevBtn = section.querySelector('.prev-btn');
            var nextBtn = section.querySelector('.next-btn');
            var dotsContainer = section.querySelector('.slider-dots');
            var cards = slider.querySelectorAll('.testimonial-card');
            
            if (!slider || !cards.length) return;

            var currentIndex = 0;
            var cardsPerView = getCardsPerView();
            var maxIndex = Math.max(0, cards.length - cardsPerView);

            // Create dots
            for (var i = 0; i <= maxIndex; i++) {
                var dot = document.createElement('div');
                dot.className = 'slider-dot' + (i === 0 ? ' active' : '');
                dot.addEventListener('click', function(index) {
                    return function() { goToSlide(index); };
                }(i));
                dotsContainer.appendChild(dot);
            }

            var dots = dotsContainer.querySelectorAll('.slider-dot');

            function getCardsPerView() {
                if (window.innerWidth <= 767) return 1;
                if (window.innerWidth <= 991) return 2;
                return 3;
            }

            function updateSlider() {
                var cardWidth = cards[0].offsetWidth + 30; // card width + gap
                slider.scrollTo({
                    left: currentIndex * cardWidth,
                    behavior: 'smooth'
                });

                // Update dots
                dots.forEach(function(dot, index) {
                    dot.classList.toggle('active', index === currentIndex);
                });

                // Update buttons
                prevBtn.disabled = currentIndex === 0;
                nextBtn.disabled = currentIndex >= maxIndex;
            }

            function goToSlide(index) {
                currentIndex = Math.max(0, Math.min(index, maxIndex));
                updateSlider();
            }

            function nextSlide() {
                if (currentIndex < maxIndex) {
                    goToSlide(currentIndex + 1);
                }
            }

            function prevSlide() {
                if (currentIndex > 0) {
                    goToSlide(currentIndex - 1);
                }
            }

            prevBtn.addEventListener('click', prevSlide);
            nextBtn.addEventListener('click', nextSlide);

            // Handle resize
            window.addEventListener('resize', function() {
                cardsPerView = getCardsPerView();
                maxIndex = Math.max(0, cards.length - cardsPerView);
                currentIndex = Math.min(currentIndex, maxIndex);
                
                // Recreate dots
                dotsContainer.innerHTML = '';
                for (var i = 0; i <= maxIndex; i++) {
                    var dot = document.createElement('div');
                    dot.className = 'slider-dot' + (i === currentIndex ? ' active' : '');
                    dot.addEventListener('click', function(index) {
                        return function() { goToSlide(index); };
                    }(i));
                    dotsContainer.appendChild(dot);
                }
                dots = dotsContainer.querySelectorAll('.slider-dot');
                updateSlider();
            });

            // Auto-play
            var autoPlayInterval = setInterval(nextSlide, 5000);

            slider.addEventListener('mouseenter', function() {
                clearInterval(autoPlayInterval);
            });

            slider.addEventListener('mouseleave', function() {
                autoPlayInterval = setInterval(nextSlide, 5000);
            });

            updateSlider();
        }

        document.addEventListener('DOMContentLoaded', function() {
            var section = document.querySelector('.testimonials-section');
            if (section) {
                initHeaderReveal(section);
                initTestimonialSlider(section);
            }
        });
    })();
    </script>

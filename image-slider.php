<script>
(function() {
    'use strict';

    function init() {
        const sliderSection = document.getElementById('projects-3d-slider');
        if (!sliderSection) return;

        const cards = sliderSection.querySelectorAll('.project-card');
        const nextBtn = sliderSection.querySelector('#projects-slider-next');
        const pagItems = sliderSection.querySelectorAll('.vertical-pag-item');
        const cardsWrapper = sliderSection.querySelector('.slider-cards-wrapper');

        let currentIndex = 0;
        const totalCards = cards.length;
        let autoplayTimer = null;

        // Mathematical Wrapping Coverflow layout updates
        function updateSlider() {
            cards.forEach((card, idx) => {
                card.classList.remove('active', 'prev', 'next');
                
                let diff = idx - currentIndex;
                
                // Wrap-around calculation for dynamic N cards
                while (diff < -Math.floor(totalCards / 2)) diff += totalCards;
                while (diff > Math.floor(totalCards / 2)) diff -= totalCards;
                
                if (diff === 0) {
                    card.classList.add('active');
                } else if (diff === 1) {
                    card.classList.add('next');
                } else if (diff === -1) {
                    card.classList.add('prev');
                }
            });

            // Update Vertical numbers indicator active class
            pagItems.forEach((item, idx) => {
                if (idx === currentIndex) {
                    item.classList.add('active');
                    // Scroll item into view in pagination list smoothly
                    item.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
                } else {
                    item.classList.remove('active');
                }
            });
        }

        function slideNext() {
            currentIndex = (currentIndex + 1) % totalCards;
            updateSlider();
        }

        function slidePrev() {
            currentIndex = (currentIndex - 1 + totalCards) % totalCards;
            updateSlider();
        }

        // Circular Next Button Event
        if (nextBtn) {
            nextBtn.addEventListener('click', function(e) {
                e.preventDefault();
                slideNext();
                resetAutoplay();
            });
        }

        // Click Card Directly to Make Active
        cards.forEach((card, idx) => {
            card.addEventListener('click', function() {
                if (currentIndex !== idx) {
                    currentIndex = idx;
                    updateSlider();
                    resetAutoplay();
                }
            });
        });

        // Vertical Pagination items click
        pagItems.forEach((item) => {
            item.addEventListener('click', function() {
                const targetIdx = parseInt(item.getAttribute('data-index'), 10);
                if (currentIndex !== targetIdx) {
                    currentIndex = targetIdx;
                    updateSlider();
                    resetAutoplay();
                }
            });
        });

        // Autoplay Loop (7 seconds)
        function startAutoplay() {
            autoplayTimer = setInterval(slideNext, 7000);
        }

        function resetAutoplay() {
            clearInterval(autoplayTimer);
            startAutoplay();
        }

        // Pause autoplay on mouse hover to improve accessibility
        sliderSection.addEventListener('mouseenter', function() {
            clearInterval(autoplayTimer);
        });

        sliderSection.addEventListener('mouseleave', function() {
            startAutoplay();
        });

        // Touch Drag / Swipe Gestures support
        let touchStartX = 0;
        let touchEndX = 0;

        if (cardsWrapper) {
            cardsWrapper.addEventListener('touchstart', function(e) {
                touchStartX = e.changedTouches[0].screenX;
            }, { passive: true });

            cardsWrapper.addEventListener('touchend', function(e) {
                touchEndX = e.changedTouches[0].screenX;
                handleSwipe();
            }, { passive: true });
        }

        function handleSwipe() {
            const threshold = 50; // pixels
            const diffX = touchStartX - touchEndX;
            if (Math.abs(diffX) > threshold) {
                if (diffX > 0) {
                    slideNext(); // Swiped left, show next
                } else {
                    slidePrev(); // Swiped right, show prev
                }
                resetAutoplay();
            }
        }

        // Initialize Carousel state
        updateSlider();
        startAutoplay();

        // 3D Parallax Interactive Effect on Cards
        if (window.innerWidth > 991) {
            sliderSection.addEventListener('mousemove', function(e) {
                const bounding = cardsWrapper.getBoundingClientRect();
                const wrapperX = bounding.left + bounding.width / 2;
                const wrapperY = bounding.top + bounding.height / 2;
                
                const distanceX = e.clientX - wrapperX;
                const distanceY = e.clientY - wrapperY;

                // Restrict parallax hover tilt to the active card
                const activeCard = sliderSection.querySelector('.project-card.active .card-inner');
                if (activeCard) {
                    const tiltX = (distanceY / (window.innerHeight / 2)) * 8; // Max 8deg tilt
                    const tiltY = -(distanceX / (window.innerWidth / 2)) * 8;
                    activeCard.style.transform = `rotateX(${tiltX}deg) rotateY(${tiltY}deg)`;
                }
            });

            sliderSection.addEventListener('mouseleave', function() {
                const activeCard = sliderSection.querySelector('.project-card.active .card-inner');
                if (activeCard) {
                    activeCard.style.transform = 'rotateX(0deg) rotateY(0deg)';
                }
            });
        }
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', init);
    } else {
        init();
    }
})();
</script>